import sys

sys.setrecursionlimit(10**6)
input = sys.stdin.readline

### ê°ê° xì¶•, yì¶• ë°©í–¥ìœ¼ë¡œ 8ê°œ ë°©í–¥
# ìƒ, ìš°ìƒ, ìš°, ìš°í•˜, í•˜, ì¢Œí•˜, ì¢Œ, ì¢Œìƒ
dirY = [-1, -1, 0, 1, 1, 1, 0, -1]
dirX = [0, 1, 1, 1, 0, -1, -1, -1]


def dfs(y, x):
    global visited, map_
    visited[y][x] = True

    for i in range(8):
        newY = y + dirY[i]
        newX = x + dirX[i]
        if map_[newY][newX] and not visited[newY][newX]:
            dfs(newY, newX)


# âœ… 0. ì…ë ¥ ë° ì´ˆê¸°í™”
while True:
    M, N = map(int, input().split())
    MAX = 50 + 10

    if N == 0 and M == 0:
        break

    # âœ… 1. mapì— ì—°ê²° ì •ë³´ ì±„ìš°ê¸°
    map_ = [[False] * MAX for _ in range(MAX)]
    visited = [[False] * MAX for _ in range(MAX)]

    for i in range(1, N + 1):
        row = list(map(int, input().split()))
        for j in range(1, M + 1):
            map_[i][j] = row[j - 1] == 1

    # âœ… 2. dfs(ì¬ê·€í•¨ìˆ˜) í˜¸ì¶œ
    answer = 0
    for i in range(1, N + 1):
        for j in range(1, M + 1):
            if map_[i][j] and not visited[i][j]:
                dfs(i, j)
                answer += 1

    # âœ… 3. ì •ë‹µ ì¶œë ¥
    print(answer)
