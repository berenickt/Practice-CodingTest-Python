import sys


# λ¬Έμμ—΄μ„ ROT13 μ•”νΈν™”ν•λ” ν•¨μλ¥Ό μ •μ
def rot13(s):
    ans = ''  # κ²°κ³Ό λ¬Έμμ—΄μ„ μ΄κΈ°ν™”

    # λ¬Έμμ—΄μ κ° λ¬Έμλ¥Ό μ²λ¦¬
    for ch in s:
        # λ¬Έμμ μ•„μ¤ν‚¤ μ½”λ“μ— 13μ„ λ”ν•μ—¬ ROT13μ„ μν–‰
        if 'a' <= ch <= 'm' or 'A' <= ch <= 'M':
            ans += chr(ord(ch)+13)
        # λ¬Έμμ μ•„μ¤ν‚¤ μ½”λ“μ—μ„ 13μ„ λΉΌμ„ ROT13μ„ μν–‰
        elif 'n' <= ch <= 'z' or 'N' <= ch <= 'Z':
            ans += chr(ord(ch)-13)
        # μ•νλ²³μ΄ μ•„λ‹ κ²½μ°, κ·Έλ€λ΅ κ²°κ³Όμ— μ¶”κ°€
        else:
            ans += ch

    return ans  # ROT13μΌλ΅ μ•”νΈν™”λ κ²°κ³Όλ¥Ό λ°ν™


