n = int(input())

# ê° ì§‘ì˜ ìƒ‰ê¹”ê³¼ ë¹„ìš©ì„ íŠœí”Œë¡œ ì €ì¥í•˜ëŠ” ë¦¬ìŠ¤íŠ¸ aë¥¼ ìƒì„±í•˜ê³  ì´ˆê¸°ê°’ ì„¤ì •
a = [(0, 0, 0)] + [tuple(map(int, input().split())) for _ in range(n)]

# ë™ì  ê³„íšë²•ì„ ìœ„í•œ ì´ì¤‘ ë¦¬ìŠ¤íŠ¸ dë¥¼ ìƒì„±í•˜ê³  ì´ˆê¸°ê°’ ì„¤ì •
d = [[0] * 3 for _ in range(n + 1)]

# ì´ˆê¸° ìµœì†Œ ë¹„ìš©ì„ ì„¤ì • (ì•„ì£¼ í° ê°’)
ans = 1000 * 1000 + 1

# ì²« ë²ˆì§¸ ì§‘ì˜ ìƒ‰ê¹”ì„ ë°”ê¿”ê°€ë©° ìµœì†Œ ë¹„ìš©ì„ ê³„ì‚°
for k in range(3):  # house1's color
    for j in range(3):
        # ì²« ë²ˆì§¸ ì§‘ì˜ ìƒ‰ê¹”ì„ ì„¤ì •
        if j == k:
            d[1][j] = a[1][j]
        else:
            d[1][j] = 1000 * 1000 + 1  # ì•„ì£¼ í° ê°’ìœ¼ë¡œ ì´ˆê¸°í™”

    # ë‘ ë²ˆì§¸ ì§‘ë¶€í„° ë§ˆì§€ë§‰ ì§‘ê¹Œì§€ ìµœì†Œ ë¹„ìš©ì„ ê³„ì‚°
    for i in range(2, n + 1):
        d[i][0] = min(d[i - 1][1], d[i - 1][2]) + a[i][0]
        d[i][1] = min(d[i - 1][0], d[i - 1][2]) + a[i][1]
        d[i][2] = min(d[i - 1][0], d[i - 1][1]) + a[i][2]

    # ë§ˆì§€ë§‰ ì§‘ì˜ ìƒ‰ê¹”ì„ ë°”ê¿”ê°€ë©° ìµœì†Œ ë¹„ìš©ì„ ê°±ì‹ 
    for j in range(3):
        if j == k:
            continue
        ans = min(ans, d[n][j])

# ìµœì†Œ ë¹„ìš© ì¶œë ¥
print(ans)
