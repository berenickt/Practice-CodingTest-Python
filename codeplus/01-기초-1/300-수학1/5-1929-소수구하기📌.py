# μ†μ νλ³„μ„ μ„ν• μµλ€ λ²”μ„λ¥Ό μ§€μ •
MAX = 1000000

# μ†μ μ—¬λ¶€λ¥Ό μ €μ¥ν•λ” λ¦¬μ¤νΈλ¥Ό μƒμ„±ν•κ³ , μ΄κΈ°κ°’μ„ Falseλ΅ μ„¤μ •
check = [0] * (MAX + 1)

# 0κ³Ό 1μ€ μ†μκ°€ μ•„λ‹λ―€λ΅ Trueλ΅ ν‘μ‹
check[0] = check[1] = True

# μ—λΌν† μ¤ν…λ„¤μ¤μ μ²΄ μ•κ³ λ¦¬μ¦μ„ μ‚¬μ©ν•μ—¬ μ†μλ¥Ό νλ³„
for i in range(2, MAX + 1):
    if not check[i]:
        j = i + i
        # iμ λ°°μλ“¤μ„ μ†μκ°€ μ•„λ‹ κ²ƒ(True)μΌλ΅ ν‘μ‹
        while j <= MAX:
            check[j] = True
            j += i

# μ…λ ¥: λ‘ μ •μ mκ³Ό nμ„ μ…λ ¥λ°›μ
m, n = map(int, input().split())

# mλ¶€ν„° nκΉμ§€μ λ²”μ„μ—μ„ μ†μλ¥Ό μ¶λ ¥
for i in range(m, n + 1):
    if check[i] == False:
        print(i)
