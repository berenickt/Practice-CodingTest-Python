import sys

# ì‚¬ìš©ìë¡œë¶€í„° nê³¼ mì„ ì…ë ¥ë°›ìŒ
n, m = map(int, input().split())

# cnt ë°°ì—´ì„ n + 1 í¬ê¸°ë¡œ ìƒì„±í•˜ê³  ëª¨ë‘ 0ìœ¼ë¡œ ì´ˆê¸°í™”
