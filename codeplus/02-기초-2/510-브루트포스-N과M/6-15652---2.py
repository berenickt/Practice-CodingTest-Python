        cnt[index] = i

        # ë‹¤ìŒ ì„ íƒëœ ìœ„ì¹˜ì—ì„œ ì¬ê·€ì ìœ¼ë¡œ í˜¸ì¶œ
        go(index + 1, selected + i, n, m)

