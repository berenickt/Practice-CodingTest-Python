    for k in range(4):  # ìƒí•˜ì¢Œìš° ë°©í–¥ì— ëŒ€í•´ì„œ
        nx, ny = x + dx[k], y + dy[k]  # ìƒˆë¡œìš´ ìœ„ì¹˜ ê³„ì‚°
        if 0 <= nx < n and 0 <= ny < m:  # ë²”ìœ„ ë‚´ì— ìˆëŠ”ì§€ í™•ì¸
            if (nx, ny) == (px, py):  # ì´ì „ ìœ„ì¹˜ë¡œ ë˜ëŒì•„ê°€ëŠ” ê²½ìš° ê±´ë„ˆë›°ê¸°
                continue
            if a[nx][ny] == color:  # ê°™ì€ ìƒ‰ìƒì˜ ê³µì´ë¼ë©´
                if go(nx, ny, x, y, color):  # ì¬ê·€ í˜¸ì¶œ
                    return True  # ì‚¬ì´í´ ë°œê²¬
    return False  # ì‚¬ì´í´ì„ ì°¾ì§€ ëª»í•œ ê²½ìš°


