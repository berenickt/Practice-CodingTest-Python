    s = sum(a[k] for k in range(n) if (i & (1 << k)) > 0)

    # λ©ν‘ ν•©κ³Ό μΌμΉν•λ” κ²½μ°, κ°€λ¥ν• κ²½μ°μ μλ¥Ό μ¦κ°€
    if m == s:
        ans += 1

