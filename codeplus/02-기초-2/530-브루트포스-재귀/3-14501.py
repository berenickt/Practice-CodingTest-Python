# π’΅ ν‡΄μ‚¬ π“ https://www.acmicpc.net/problem/14501
inf = -(10**9)
n = int(input())
t = [0] * (n + 1)
p = [0] * (n + 1)

# κ° λ‚ μ§μ— λ€ν• μƒλ‹΄ μ‹κ°„κ³Ό λ³΄μ μ…λ ¥
for i in range(1, n + 1):
    t[i], p[i] = map(int, input().split())

ans = 0


# κ°€λ¥ν• λ¨λ“  μΌμ •μ„ ν™•μΈν•λ©° μµλ€ λ³΄μλ¥Ό κµ¬ν•λ” ν•¨μ
def go(day, s):
    global ans

    # λ¨λ“  μΌμ •μ„ ν™•μΈν• κ²½μ° μµλ€ λ³΄μ κ°±μ‹ 
    if day == n + 1:
        ans = max(ans, s)
        return

    # μ΄λ―Έ λ§κ°μΌμ΄ μ§€λ‚ κ²½μ°μ™€ μ•„μ§ λ§κ°μΌμ΄ μ§€λ‚μ§€ μ•μ€ κ²½μ°λ¥Ό κ°κ° κ³ λ ¤
    if day > n + 1:
        return

    # ν„μ¬ μΌμ •μ„ μ„ νƒν•μ§€ μ•κ³  λ‹¤μ λ‚ μ§λ΅ μ§„ν–‰
    go(day + 1, s)

    # ν„μ¬ μΌμ •μ„ μ„ νƒν•κ³  ν•΄λ‹Ή μΌμ • μΆ…λ£μΌμ— λ³΄μλ¥Ό λ”ν•¨
    go(day + t[day], s + p[day])


# μ²« λ‚ λ¶€ν„° μ‹μ‘ν•μ—¬ μµλ€ λ³΄μ κ³„μ‚°
go(1, 0)
print(ans)
