from collections import deque

# ìƒí•˜ì¢Œìš° ì´ë™ì„ ìœ„í•œ dx, dy ë°°ì—´ ì´ˆê¸°í™”
dx = [0, 0, 1, -1]
dy = [1, -1, 0, 0]

# ì…ë ¥ ë°›ê¸°
m, n = map(int, input().split())  # ê°€ë¡œ, ì„¸ë¡œ í¬ê¸° ì…ë ¥
a = [list(map(int, list(input()))) for _ in range(n)]  # ë¯¸ë¡œ ì •ë³´ ì…ë ¥

# ê±°ë¦¬ ì •ë³´ë¥¼ ì €ì¥í•  2ì°¨ì› ë°°ì—´ ì´ˆê¸°í™”
dist = [[-1] * m for _ in range(n)]

# í ìƒì„± ë° ì‹œì‘ ìœ„ì¹˜ ì´ˆê¸°í™”
q = deque()
next_queue = deque()
q.append((0, 0))  # ì‹œì‘ ìœ„ì¹˜ (0, 0)
dist[0][0] = 0  # ì‹œì‘ ìœ„ì¹˜ì˜ ê±°ë¦¬ëŠ” 0ìœ¼ë¡œ ì„¤ì •

while q:
    x, y = q.popleft()  # íì—ì„œ í˜„ì¬ ìœ„ì¹˜ êº¼ë‚´ê¸°
    for k in range(4):  # ìƒí•˜ì¢Œìš° ì´ë™ì„ ìœ„í•œ ë°˜ë³µë¬¸
        nx, ny = x + dx[k], y + dy[k]  # ì´ë™í•  ë‹¤ìŒ ìœ„ì¹˜ ê³„ì‚°
        if 0 <= nx < n and 0 <= ny < m:  # ë¯¸ë¡œ ë²”ìœ„ ë‚´ì—ì„œë§Œ ì²˜ë¦¬
            if dist[nx][ny] == -1:  # ì•„ì§ ë°©ë¬¸í•˜ì§€ ì•Šì€ ìœ„ì¹˜ë¼ë©´
                if a[nx][ny] == 0:  # ì´ë™ ê°€ëŠ¥í•œ ìœ„ì¹˜ì¸ ê²½ìš°
                    dist[nx][ny] = dist[x][y]  # ê±°ë¦¬ ì •ë³´ ì—…ë°ì´íŠ¸ (ì´ë™í•˜ì§€ ì•Šì•˜ìœ¼ë¯€ë¡œ ê±°ë¦¬ ê·¸ëŒ€ë¡œ)
                    q.append((nx, ny))  # ë‹¤ìŒ ìœ„ì¹˜ë¥¼ íì— ì¶”ê°€
                else:  # ë²½(1)ì¸ ê²½ìš°
                    dist[nx][ny] = dist[x][y] + 1  # ê±°ë¦¬ ì •ë³´ ì—…ë°ì´íŠ¸ (ë²½ì„ í•œ ë²ˆ ëš«ì—ˆìœ¼ë¯€ë¡œ ê±°ë¦¬ +1)
                    next_queue.append((nx, ny))  # ë‹¤ìŒ ìœ„ì¹˜ë¥¼ ë‹¤ìŒ íì— ì¶”ê°€

    if not q:  # í˜„ì¬ íê°€ ë¹„ì—ˆë‹¤ë©´ (ë‹¤ìŒ íë¡œ êµì²´)
        q = next_queue
        next_queue = deque()

print(dist[n - 1][m - 1])  # ëª©í‘œ ìœ„ì¹˜ê¹Œì§€ì˜ ìµœë‹¨ ê±°ë¦¬ ì¶œë ¥
