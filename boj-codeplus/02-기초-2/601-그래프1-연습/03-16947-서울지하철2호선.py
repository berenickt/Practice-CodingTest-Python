# π’΅ μ„μΈ μ§€ν•μ²  2νΈμ„  π“ https://www.acmicpc.net/problem/16947
from collections import deque
import sys

sys.setrecursionlimit(1000000)
n = int(input())  # λ…Έλ“ κ°μ μ…λ ¥
a = [[] for _ in range(n)]  # λΉ μΈμ ‘ λ¦¬μ¤νΈ μ΄κΈ°ν™”

for _ in range(n):
    u, v = map(int, input().split())  # κ°„μ„  μ •λ³΄ μ…λ ¥
    u -= 1  # 1λ¶€ν„° μ‹μ‘ν•λ” μΈλ±μ¤λ¥Ό 0λ¶€ν„° μ‹μ‘ν•λ” μΈλ±μ¤λ΅ λ³€κ²½
    v -= 1  # 1λ¶€ν„° μ‹μ‘ν•λ” μΈλ±μ¤λ¥Ό 0λ¶€ν„° μ‹μ‘ν•λ” μΈλ±μ¤λ΅ λ³€κ²½
    a[u].append(v)  # μ–‘λ°©ν–¥ κ°„μ„  μ¶”κ°€
    a[v].append(u)  # μ–‘λ°©ν–¥ κ°„μ„  μ¶”κ°€

check = [0] * n  # λ…Έλ“μ λ°©λ¬Έ μ—¬λ¶€ λ° μ‚¬μ΄ν΄ μ—¬λ¶€λ¥Ό μ €μ¥ν•λ” λ°°μ—΄ μ΄κΈ°ν™”
# 0: λ°©λ¬Έν•μ§€ μ•μ, 1: λ°©λ¬Έν–μΌλ‚ μ‚¬μ΄ν΄μ— μ†ν•μ§€ μ•μ, 2: λ°©λ¬Έν•λ©° μ‚¬μ΄ν΄μ— μ†ν•¨


def go(x, p):
    # -2: μ‚¬μ΄ν΄ λ°κ²¬ν•μ€μΌλ‚ ν¬ν•¨λμ§€ μ•μ
    # -1: μ‚¬μ΄ν΄μ„ μ°Ύμ§€ λ»ν•¨
    # 0~n-1: μ‚¬μ΄ν΄ λ°κ²¬ν•μ€μΌλ©° μ‹μ‘ μΈλ±μ¤

    if check[x] == 1:  # μ΄λ―Έ λ°©λ¬Έν• λ…Έλ“μΈ κ²½μ°
        return x

    check[x] = 1  # λ…Έλ“ λ°©λ¬Έ μ²λ¦¬
    for y in a[x]:
        if y == p:  # λ¶€λ¨ λ…Έλ“λ΅ λλμ•„κ°€λ” κ²½μ°λ” κ±΄λ„λ›°κΈ°
            continue
        res = go(y, x)  # μ¬κ·€ νΈμ¶
        if res == -2:
            return -2
        if res >= 0:
            check[x] = 2
            if x == res:  # ν„μ¬ λ…Έλ“κ°€ μ‚¬μ΄ν΄μ μ‹μ‘ λ…Έλ“μΈ κ²½μ°
                return -2  # μ‚¬μ΄ν΄μ— μ†ν•μ§€ μ•μ
            else:
                return res  # μ‚¬μ΄ν΄μ μ‹μ‘ λ…Έλ“ μΈλ±μ¤ λ°ν™
    return -1  # μ‚¬μ΄ν΄μ„ μ°Ύμ§€ λ»ν• κ²½μ°


go(0, -1)  # DFSλ¥Ό ν†µν•΄ μ‚¬μ΄ν΄ μ—¬λ¶€ ν™•μΈ

q = deque()  # ν μ΄κΈ°ν™”
dist = [-1] * n  # κ±°λ¦¬ μ •λ³΄λ¥Ό μ €μ¥ν•λ” λ°°μ—΄ μ΄κΈ°ν™”

for i in range(n):
    if check[i] == 2:  # μ‚¬μ΄ν΄μ— μ†ν• λ…Έλ“μΈ κ²½μ°
        dist[i] = 0  # κ±°λ¦¬ μ΄κΈ°ν™”
        q.append(i)  # νμ— μ‹μ‘ λ…Έλ“ μ¶”κ°€
    else:
        dist[i] = -1  # μ‚¬μ΄ν΄μ— μ†ν•μ§€ μ•μ€ λ…Έλ“μ κ±°λ¦¬ μ΄κΈ°ν™”

while q:
    x = q.popleft()  # νμ—μ„ λ…Έλ“ κΊΌλ‚΄κΈ°
    for y in a[x]:
        if dist[y] == -1:  # μ•„μ§ λ°©λ¬Έν•μ§€ μ•μ€ λ…Έλ“μΈ κ²½μ°
            q.append(y)  # νμ— μ¶”κ°€
            dist[y] = dist[x] + 1  # κ±°λ¦¬ μ •λ³΄ μ—…λ°μ΄νΈ

print(*dist, sep=" ")  # κ²°κ³Ό μ¶λ ¥
