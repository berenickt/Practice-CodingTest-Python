from collections import deque

dx = [0, 0, 1, -1]  # ì´ë™ ë°©í–¥: ì˜¤ë¥¸ìª½, ì™¼ìª½, ì•„ë˜, ìœ„
dy = [1, -1, 0, 0]  # ì´ë™ ë°©í–¥: ì˜¤ë¥¸ìª½, ì™¼ìª½, ì•„ë˜, ìœ„

n, m = map(int, input().split())  # ì…ë ¥: í–‰ê³¼ ì—´ì˜ í¬ê¸° n, m
a = [list(map(int, list(input()))) for _ in range(n)]  # ì…ë ¥: 2ì°¨ì› ë°°ì—´ a
q = deque()  # í ì´ˆê¸°í™”
check = [[False] * m for _ in range(n)]  # ë°©ë¬¸ ì—¬ë¶€ë¥¼ ì €ì¥í•˜ëŠ” 2ì°¨ì› ë°°ì—´ ì´ˆê¸°í™”
