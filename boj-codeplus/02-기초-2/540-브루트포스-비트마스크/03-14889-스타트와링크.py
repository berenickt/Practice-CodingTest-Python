n = int(input())

# ì…ë ¥: ëŠ¥ë ¥ì¹˜ í–‰ë ¬
s = [list(map(int, input().split())) for _ in range(n)]

# ê²°ê³¼ë¥¼ ì €ì¥í•  ë³€ìˆ˜, ì´ˆê¸°ê°’ì€ -1ë¡œ ì„¤ì •
ans = -1

# ëª¨ë“  ê°€ëŠ¥í•œ íŒ€ ì¡°í•©ì„ ê²€ì‚¬í•˜ê¸° ìœ„í•œ ë°˜ë³µë¬¸
for i in range((1 << n)):
    first = []  # ì²« ë²ˆì§¸ íŒ€ì˜ ë©¤ë²„ë¥¼ ì €ì¥í•˜ëŠ” ë¦¬ìŠ¤íŠ¸
    second = []  # ë‘ ë²ˆì§¸ íŒ€ì˜ ë©¤ë²„ë¥¼ ì €ì¥í•˜ëŠ” ë¦¬ìŠ¤íŠ¸

    for j in range(n):
        if (i & (1 << j)) > 0:
            first += [j]  # ë¹„íŠ¸ë§ˆìŠ¤í¬ë¥¼ ì‚¬ìš©í•˜ì—¬ íŒ€ ë©¤ë²„ë¥¼ êµ¬ë¶„
        else:
            second += [j]

    # íŒ€ì˜ í¬ê¸°ê°€ ë°˜ì”© ë‚˜ë‰˜ì§€ ì•Šìœ¼ë©´ ìŠ¤í‚µ
    if len(first) != n // 2:
        continue

    t1 = 0  # ì²« ë²ˆì§¸ íŒ€ì˜ ëŠ¥ë ¥ì¹˜ í•©ì„ ì €ì¥í•˜ëŠ” ë³€ìˆ˜
    t2 = 0  # ë‘ ë²ˆì§¸ íŒ€ì˜ ëŠ¥ë ¥ì¹˜ í•©ì„ ì €ì¥í•˜ëŠ” ë³€ìˆ˜

    for l1 in range(n // 2):
        for l2 in range(n // 2):
            if l1 == l2:
                continue
            t1 += s[first[l1]][first[l2]]  # ì²« ë²ˆì§¸ íŒ€ì˜ ëŠ¥ë ¥ì¹˜ í•© ê³„ì‚°
            t2 += s[second[l1]][second[l2]]  # ë‘ ë²ˆì§¸ íŒ€ì˜ ëŠ¥ë ¥ì¹˜ í•© ê³„ì‚°

    # ë‘ íŒ€ ëŠ¥ë ¥ì¹˜ ì°¨ì´ì˜ ì ˆëŒ€ê°’ ê³„ì‚°
    diff = abs(t1 - t2)

    # í˜„ì¬ ì°¨ì´ê°€ ìµœì†Œì¸ ê²½ìš° ans ì—…ë°ì´íŠ¸
    if ans == -1 or ans > diff:
        ans = diff

