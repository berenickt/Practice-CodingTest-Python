def solution(progresses, speeds):
    Q = []
    for pro, spe in zip(progresses, speeds):
        if len(Q) == 0 or Q[-1][0] < -((pro - 100) // spe):
            Q.append([-((pro - 100) // spe), 1])
        else:
            Q[-1][1] += 1
    return [q[1] for q in Q]
