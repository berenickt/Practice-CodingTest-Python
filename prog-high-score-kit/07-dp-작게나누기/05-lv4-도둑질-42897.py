def solution(money):
    a = [money[0], money[0]]
    b = [0, money[1]]

    for i in range(2, len(money) - 1):
        a.append(max(a[i - 2] + money[i], a[i - 1]))
        b.append(max(b[i - 2] + money[i], b[i - 1]))
    answer = max(a[-1], b[-2] + money[-1])

    return answer


print(solution([1, 2, 3, 1]))
